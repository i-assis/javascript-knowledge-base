// THERE ARE MANY ATTRIBUTION OPERATORS

const a = 7; // the equal sign is making an attribution
// * it puts seven into the constant a


let b = 3; // another attribution

// ! ATTRIBUTIONS + ARITHMETICS - SHORTCUTS

b += a; // b = b + a

console.log(b)

b -= a; // b = b - a

console.log(b)

b *= 2; // b = b * 2

console.log(b)

b %= 2 // b = b / 2 // ! This is modular attribution b mod 2 = b % 2 (the rest of the euclidian division b / 2)

console.log(b)

